package 출력;

public class Ex01자바사용해보기 {

	// main method (메인기능) : 이 프로그램의 시작점
	public static void main(String[] args) {
		// 프로그램의 시작점은 main method 안이기 때문에 코드는 main 안쪽에서 적기!
		System.out.println("Hello World!");
		// 출력 자동완성 : syso + ctrl + space -> sysout 찾아서 엔터
		System.out.println("민희진");
		// 프로그램 실행시키는 단축키 : Ctrl + F11
		// 실행시키면 console 탭에 출력문이나 오류 메시지가 나온다!

	}

}
